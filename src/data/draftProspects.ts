export function prospectsAtCollege(collegeIdOrName: string): DraftProspect[] {
  const key = collegeIdOrName.toLowerCase();
  return draftProspects.filter(
    (p) =>
      (p.collegeId && p.collegeId === collegeIdOrName) ||
      p.college.toLowerCase() === key ||
      p.college.toLowerCase().includes(key),
  );
}
